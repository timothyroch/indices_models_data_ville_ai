"""
Public API for Phase 6 recurrent temporal memory encoding.

Target path:
    urban_resilience_models/
        v2_hazard_conditioned_functional_ugnn/
            memory/
                recurrent_memory_encoder/
                    __init__.py

This package exposes the complete public Phase 6 recurrent-memory surface:

- immutable execution metadata, state-layout, and run schemas;
- history-length derivation and stable execution-order planning;
- shared pointwise input adaptation;
- exact-zero recurrent initial-state construction;
- canonicalization, packing, unpacking, and restoration utilities;
- GRU and LSTM temporal sequence encoders;
- detached numerical and structural diagnostics;
- the thin recurrent dispatcher and ``RecurrentMemoryEncoder`` facade.

The private ``_provenance`` module is intentionally not re-exported. Parameter
snapshot hashing, architecture provenance construction, and computation
lineage assembly remain implementation details consumed through the public
encoder methods.

Importing this package constructs no model, allocates no trainable parameter,
executes no recurrent kernel, mutates no registry, and creates no parameter
snapshot.
"""

